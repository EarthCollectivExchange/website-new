// check-local-test-message-flow.mjs
// Pass 137 — Local Test Message Flow + Send Gate Clarity
// Asserts that create-local-test-message is properly defined, that unknown-trust
// users can create test messages but cannot send real messages, and that all
// i18n keys and UI wording are in place.

import { readFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

function readOrFail(rel) {
  try {
    return readFileSync(join(root, rel), 'utf8');
  } catch {
    console.error(`FAIL [file-missing]: ${rel}`);
    process.exit(1);
  }
}

let passed = 0;
let failed = 0;

function assert(label, condition) {
  if (condition) {
    console.log(`  PASS  ${label}`);
    passed++;
  } else {
    console.error(`  FAIL  ${label}`);
    failed++;
  }
}

// ─── Load files ───────────────────────────────────────────────────────────────

const consentEngine    = readOrFail('lib/qlpa/consentEngine.ts');
const emptyJourney     = readOrFail('components/messaging/EmptyConversationJourney.tsx');
const conversationView = readOrFail('components/messaging/ConversationView.tsx');
const enLocale         = readOrFail('lib/i18n/locales/en.json');

const locales = ['en', 'fr', 'de', 'es', 'it', 'pt', 'id'].map(lang => ({
  lang,
  content: readOrFail(`lib/i18n/locales/${lang}.json`),
}));

// ─── [1] create-local-test-message action exists in ConsentAction union ───────

console.log('\n[1] create-local-test-message in ConsentAction union');
assert(
  'ConsentAction includes create-local-test-message',
  consentEngine.includes("'create-local-test-message'")
);

// ─── [2] create-local-test-message has min trust = self ───────────────────────

console.log('\n[2] create-local-test-message ACTION_MIN_TRUST = self');
assert(
  "ACTION_MIN_TRUST['create-local-test-message'] = 'self'",
  consentEngine.includes("'create-local-test-message':  'self'") ||
  consentEngine.includes("'create-local-test-message': 'self'")
);

// ─── [3] send-message still requires known ────────────────────────────────────

console.log('\n[3] send-message ACTION_MIN_TRUST still requires known');
assert(
  "'send-message': 'known'",
  consentEngine.includes("'send-message':") && consentEngine.includes("'known'")
);
assert(
  'send-message is not loosened to self or unknown',
  !consentEngine.includes("'send-message':  'self'") &&
  !consentEngine.includes("'send-message': 'self'") &&
  !consentEngine.includes("'send-message':  'unknown'") &&
  !consentEngine.includes("'send-message': 'unknown'")
);

// ─── [4] EmptyConversationJourney uses new i18n keys ─────────────────────────

console.log('\n[4] EmptyConversationJourney uses createLocalTestMessage keys');
assert(
  "uses emptyJourney.createLocalTestMessage label",
  emptyJourney.includes('emptyJourney.createLocalTestMessage')
);
assert(
  "uses emptyJourney.createLocalTestMessageBody",
  emptyJourney.includes('emptyJourney.createLocalTestMessageBody')
);

// ─── [5] EmptyConversationJourney Step 2 is not disabled by !hasMembers ───────

console.log('\n[5] Step 2 CTA is not gated on hasMembers');
assert(
  'ctaDisabled={false} — not gated on hasMembers',
  emptyJourney.includes('ctaDisabled={false}')
);
assert(
  'inviteFirstHint removed from Step 2',
  !emptyJourney.includes('inviteFirstHint')
);

// ─── [6] Duplicate guard still present ───────────────────────────────────────

console.log('\n[6] Duplicate guard still in ConversationView');
assert(
  'contentKind === generated guard exists',
  conversationView.includes("m.contentKind === 'generated'")
);

// ─── [7] ConversationView uses generated bypass ───────────────────────────────

console.log('\n[7] ConversationView generated bypass path');
assert(
  'isGeneratedMessage bypass exists',
  conversationView.includes('isGeneratedMessage')
);
assert(
  "contentKind: 'generated' passed in onSendTestMessage",
  conversationView.includes("contentKind: 'generated'")
);

// ─── [8] No fetch/XMLHttpRequest/supabase in generated path ──────────────────

console.log('\n[8] Local test message path does not reach relay (no explicit supabase call in bypass)');
// The bypass exits before reaching relay evaluation — verified structurally
assert(
  'isGeneratedMessage: no relay gate call path',
  conversationView.includes('isGeneratedMessage') &&
  !conversationView.includes('supabase.from') // supabase.from is in actions.ts, not ConversationView
);

// ─── [9] Send gate notice shown for unknown trust ────────────────────────────

console.log('\n[9] Send gate notice shown for unknown trust');
assert(
  "SendGateNotice rendered when trustLevel === 'unknown'",
  conversationView.includes("convSettings.trustLevel === 'unknown'") &&
  conversationView.includes('SendGateNotice')
);
assert(
  'sendGate.trustRequiredBody used in SendGateNotice',
  conversationView.includes('sendGate.trustRequiredBody')
);
assert(
  'sendGate.inviteMember used in SendGateNotice',
  conversationView.includes('sendGate.inviteMember')
);

// ─── [10] i18n keys present in all 7 locales ─────────────────────────────────

console.log('\n[10] i18n keys present in all 7 locales');

const REQUIRED_KEYS = [
  '"createLocalTestMessage"',
  '"createLocalTestMessageBody"',
  '"localTestCreated"',
  '"trustRequiredTitle"',
  '"trustRequiredBody"',
  '"inviteMember"',
  '"clearLocalTestConversations"',
];

for (const { lang, content } of locales) {
  for (const key of REQUIRED_KEYS) {
    assert(
      `${lang}: ${key}`,
      content.includes(key)
    );
  }
}

// ─── Summary ──────────────────────────────────────────────────────────────────

console.log(`\n──────────────────────────────────────────`);
console.log(`Pass 137 check: ${passed} passed, ${failed} failed`);

if (failed > 0) {
  process.exit(1);
}
